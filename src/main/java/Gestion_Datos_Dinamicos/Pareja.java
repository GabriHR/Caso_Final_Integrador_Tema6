package Gestion_Datos_Dinamicos;

public class Pareja {
    private int primero;
    private int segundo;

    public Pareja(int primero, int segundo) {
        this.primero = primero;
        this.segundo = segundo;
    }

    public int getPrimero() {
        return primero;
    }

    public int getSegundo() {
        return segundo;
    }
}